
<h2>Rules of the Game</h2>
<p>
In the following section the rules are listed in German, followed by the English translation below.
</p>

<h3>§1 - Vorbereitung</h3>
<ul>
  <li>a) Zu Beginn erstellt jede/r Teilnehmer/in ein Ranking `R_own` der Nationalteams bezüglich der erwarteten Gewinnchancen beim FIFA World Cup 2018, indem er eine Reihenfolge der Nationen angibt. Das Ranking bleibt zunächst geheim, bis alle Rankings abgegeben wurden.</li>
  <li>b) Anschließend werden die Rankings aller Teilnehmer/innen veröffentlicht und es wird das gemeinsame Ranking `R_common` durch Rangaddition gebildet. Wo mehrere Nationen dieselbe Position belegen würden, wird die alphabetische Reihenfolge der englischen Landesbezeichnung verwendet.</li>
  <li>c) Jeder Nation wird ein Wert zugeordnet, der für die Dauer des Wettbewerbs unveränderlich ist. Das am stärksten platzierte Team bekommt den Wert 32, die nachfolgenden Teams bekommen absteigend die Werte 31, 30, usw.</li>
</ul>

<h3>§2 - Gewählte Nationen</h3>
<ul>
  <li>a) Jede/r Teilnehmer/in wählt vor Beginn des Turniers 4 Nationen so, dass die Summe ihrer Werte kleiner gleich 60 ist.</li>
  <li>b) In begründeten Ausnahmefällen darf sich der/die Teilnehmer/in beim Bilden der Summe durch Kopfrechnen um 1 verrechnen.</li>
</ul>  


<h3>§3 - Der/Die Teilnehmer/in erzielt bei folgenden Ereignissen Gewinnpunkte: </h3>
<ul>
  <li>a) Wann immer eine der aktuell gewählten Nationen unentschieden spielt, wird der Wert des Gegners gutgeschrieben.</li>
  <li>b) Wann immer eine der aktuell gewählten Nationen gewinnt, wird der doppelte Wert des Gegners gutgeschrieben.</li>
</ul>
  
<h3>§4 - Auswechseln von Nationen</h3>
Nach Abschluss der Gruppenphase kann man seine Auswahl der 4 Nationen nach folgenden Bestimmungen ändern: 
<ul>
  <li>a) Für jede Nation wird die aktuelle Summe ihrer bisher eingespielten Anzahl Punkte `S` gemäß den Regeln in §3 gebildet (unabhängig davon, ob die Nation ausgewählt war oder nicht). Mit dieser Summe wird ein aktuelles Ranking `R_current` aller Nationen erstellt.</li>
  <li>b) "Besserwisser-Regel": 
    Eine Nation kann nur hinzugewählt werden, wenn sie zum aktuellen Zeitpunkt in `R_current` besser als in `R_common` und in `R_own` besser als in `R_common` gerankt ist. Also nur, wenn der/die Teilnehmer/in die Nation besser eingeschätzt hat als der Durchschnitt und es tatsächlich so eingetreten ist. Außerdem darf man die Nation nicht schon besitzen.</li>
  <li>c) Der zu bezahlende Preis beträgt gleich der Summe der bisher erzielten Punkte `S` der Nation. Er wird dem/der Teilnehmer/in von den gesammelten Gewinnpunkten abgezogen. (Rechnung wird automatisch und nicht im Kopf durchgeführt!) Dieses Abziehen von Punkten passiert, sobald die Nation "gekauft" wird. Darüberhinaus ändert sich der Punktestand durch diesen Vorgang nicht.</li>
  <li>d) Wird eine Nation eingewechselt (zur Auswahl hinzugenommen), muss gleichzeitig im Austausch eine andere der gewählten Nationen ausgewechselt (von der Auswahl entfernt) werden, sodass jede/r Teilnehmer/in zu jeder Zeit 4 Nationen hat. Die durch die zu entfernende Nation bisher gesammelten Punkte darf man behalten.</li>
  <li>e) Das Auswechseln von Nationen ist nicht möglich, während ein Spiel läuft. Genauer, solange nicht, bis die Ergebnisse des Spiels im online Tipp-System eingetragen wurden (weil sich dadurch die Preise ändern können). Das passiert aber automatisch, deshalb sollte die Verzögerung sehr kurz sein (eventuell Seite neu laden!). Ansonsten darf man so oft man will die Nationen tauschen, auch die gleiche Nation mehrmals ein- und wieder auswechseln. Es kostet nur jedesmal die entsprechenden Punkte.</li>
</ul>


<h3>§5 - Sieger/in</h3>
<ul>
  <li>a) Nach dem Wettbewerb ist der/diejenige Sieger/in, der/die die meisten Gewinnpunkte gesammelt hat. </li>
  <li>b) Bei Gleichstand entscheidet der Geburtszeitpunkt der älteren Großmutter (früher gewinnt, im Zweifelsfall amtlichen Nachweis vorlegen).</li>
</ul>

