<h3>§1 - Preparation</h3>
<ul>
  <li>a) Initially every participants defines a ranking, `R_own`, of the national teams depending on their expected chances at the FIFA World Cup 2018 by entering the order on the web page. This ranking is kept secret until all users' rankings are given.</li>
  <li>b) Then all rankings become public and the common ranking, `R_common`, is computed by rank addition. If there would be multiple nations on the same rank, the alphabetic order of the names is used instead.</li>
  <li>c) Every nations gets a value which will be constant for the whole contest. The national team on the first rank gets the value 32, the following teams get a by one decreasing value of 31, 30 etc.</li>
</ul>

<h3>§2 - Choosing nations</h3>
<ul>
  <li>a) Every participant chooses 4 nations before the world cup starts. The sum of the values of these 4 nations has to be smaller or equal to 60 (except when applying the rule §2b)</li>
  <li>b) If the participant fails to do the mental computation of 4 numbers and get a sum of 61 where the correct sum would be 60, this will be accepted, if the participant provides an excuse for their disability.</li>
</ul>  

<h3>§3 - Computation of the score </h3>
<ul>
  <li>a) If any of the choosen nations plays a match that results in a draw, the participants get the value of the opponent added to their score.</li>
  <li>b) If any of the choosen nations wins match, the participants get twice the value of the opponent added to their score.</li>
</ul>
  
<h3>§4 - Trading nations</h3>
After completion of the group phase the selection of 4 nations can be changed by trading one nation for another:
<ul>
  <li>a) For every nation the sum of its previously scored points, `S`, is computed by the rules in §3 (not taking into account if the nation was chosen or not). Using these sums a current ranking, `R_current`, of all nations is computed.</li>
  <li>b) "Clever-Clogs-Rule": 
    A nation can only be added, if a the time, it is ranked better in `R_current` than in `R_common`  and also better in `R_own` than in `R_common`. That means that people who knew it advance that some teams would perform better than the average of participants did are now rewarded by having more opportunities to exchange teams.
  </li>
  <li>c) The price of the nation that should be bought is equal to the sum of previously scored points, `S` of this nation. It will be substracted from the users' score.</li>
  <li>d) If a nation is to be added at the same time one of the currently selected nations has to be dropped, such that every participant has exactly 4 nations at any time of the contest.</li>
  <li>e) Selecting and dropping nations is not possible while a match is being played, you have to wait until the match is finished and the results are entered into the system.</li>
</ul>

<h3>§5 - Winner</h3>
<ul>
  <li>a) After the contest the winner is the participant with the highest score.</li>
  <li>b) If there is a draw, date and time of birth of the oldest grandmother is the tie breaker, the participant with the older grandmother wins (in case of doubt an official document has to be provided).</li>
</ul>

